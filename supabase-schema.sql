-- テーブル作成
create table symptoms (
  id text primary key,
  name text not null,
  description text,
  sort_order integer
);

create table nutrients (
  id text primary key,
  name text not null,
  category text not null,
  description text,
  foods text[],
  daily_intake text,
  sort_order integer
);

create table symptom_nutrients (
  symptom_id text references symptoms(id) on delete cascade,
  nutrient_id text references nutrients(id) on delete cascade,
  reason text,
  primary key (symptom_id, nutrient_id)
);

